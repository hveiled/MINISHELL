#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t len)
{
	char		*d;
	const char	*s;

	d = (char *)dst;
	s = (const char *)src;
	if (!len || d == s)
		return (dst);
	while (len--)
		*d++ = *s++;
	return (dst);
}
